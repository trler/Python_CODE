# structured query language 标准化查询语言

# 基于对表、库、数据的管理，账户与权限的管理功能，操作数据库的SQL语言分为 DDL DML DCL DQL
# 数据定义definition 数据操纵manipulation 数据控制 control 数据查询 query
# 几个特征 1，大小写不敏感
# 2，分号结尾，可以支持多行书写


# DDL 库管理
# show databases;   查看数据库
# use   使用数据库
# create database 名字 charset utf8  创建数据库
# drop database 数据库名称
# select database() 查看当前使用的数据库


# DDL 表管理
# show table 查看当前选择数据库的表
# create table 表名称(
#     列名称 列类型
# )
# drop table 表名称;

